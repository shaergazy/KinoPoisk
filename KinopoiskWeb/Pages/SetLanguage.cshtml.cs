using Microsoft.AspNetCore.Localization;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;

namespace KinopoiskWeb.Pages
{
    [ResponseCache(Duration = 0, Location = ResponseCacheLocation.None, NoStore = true)]
    public class SetLanguageModel : PageModel
    {
        [BindProperty]
        public string Culture { get; set; }

        [BindProperty(SupportsGet = true)]
        public string ReturnUrl { get; set; }

        public void OnGet(string returnUrl)
        {
            ReturnUrl = string.IsNullOrEmpty(returnUrl) ? Url.Page("/Index") : returnUrl;
        }

        public IActionResult OnPost()
        {
            // ??????? ?????? ????, ???? ??? ??????????
            if (Request.Cookies.ContainsKey(CookieRequestCultureProvider.DefaultCookieName))
            {
                Response.Cookies.Delete(CookieRequestCultureProvider.DefaultCookieName);
            }

            // ????????????? ????? ???? ? ????????? ?????????
            Response.Cookies.Append(
                CookieRequestCultureProvider.DefaultCookieName,
                CookieRequestCultureProvider.MakeCookieValue(new RequestCulture(Culture)),
                new CookieOptions
                {
                    Expires = DateTimeOffset.UtcNow.AddYears(1),
                    IsEssential = true, // ????? ??? GDPR, ????? ???? ?? ???? ?????????????
                    SameSite = SameSiteMode.Lax, // ????????? ?????? ? ?????? ????????
                    Secure = true, // ???????????? ??? https
                    HttpOnly = false // ???????? ??? JavaScript, ???? ??????????
                }
            );

            // ???????????? ?? ???????? ? ??????? ??? ?????? ??????
            return LocalRedirect(ReturnUrl);
        }

        public bool IsSelected(string culture)
        {
            return Culture == culture;
        }
    }
}
